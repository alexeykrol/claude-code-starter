    cp "$TEMP_DIR/framework/FRAMEWORK_GUIDE.md" ./
    log_success "Installed FRAMEWORK_GUIDE.md"

    # Copy .claude/ directory
    mkdir -p .claude
    cp -r "$TEMP_DIR/framework/.claude/"* .claude/
    log_success "Installed .claude/ directory"
}

