setup_gitignore() {
    log_info "Setting up .gitignore..."

    if [ ! -f ".gitignore" ]; then
        touch .gitignore
    fi

    # Add framework-specific ignores
    local IGNORES=(
        "dialog/"
        "html-viewer/"
        ".claude/.last_session"
        ".claude/.backup-*"
    )

    for ignore in "${IGNORES[@]}"; do
        if ! grep -q "^$ignore" .gitignore 2>/dev/null; then
            echo "$ignore" >> .gitignore
        fi
    done

    log_success "Updated .gitignore"
}

